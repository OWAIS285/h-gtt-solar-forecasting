    test_rmse = np.sqrt(mean_squared_error(y_true_test, y_pred_test))
    test_r2 = r2_score(y_true_test, y_pred_test)

    print("\n=== FINAL TEST PERFORMANCE (HybridGTT) ===")
    print(f"MAE (GWh):  {test_mae:.4f}")
    print(f"RMSE (GWh): {test_rmse:.4f}")
    print(f"R²:         {test_r2:.4f}")

    return model, (X_test, edge_index_test, target_mean, target_std, scaler,
               y_trainval_raw, y_test_raw, test_mae, test_rmse, test_r2)

