def train_xgboost_baseline(X_trainval_raw, y_trainval_raw,
                           X_test_raw, y_test_raw):
    """
    *_raw: numpy arrays in ORIGINAL scale (not normalized).
    Use the SAME temporal split as your main model:
    - trainval = first 80%
    - test     = last 20%
    """
    # Build lag features on trainval and test separately
    X_train_lags, y_train_lags = create_lag_features(X_trainval_raw, y_trainval_raw)
    X_test_lags, y_test_lags   = create_lag_features(X_test_raw, y_test_raw)

    # Standardize lag features
    scaler = StandardScaler()
    X_train_lags = scaler.fit_transform(X_train_lags)
    X_test_lags  = scaler.transform(X_test_lags)

    model = XGBRegressor(
        n_estimators=200,
        max_depth=5,
        learning_rate=0.05,
        subsample=0.8,
        colsample_bytree=0.8,
        objective="reg:squarederror",
        random_state=42
    )
    model.fit(X_train_lags, y_train_lags)

    y_pred = model.predict(X_test_lags)

    mae = mean_absolute_error(y_test_lags, y_pred)
    rmse = np.sqrt(mean_squared_error(y_test_lags, y_pred))
    r2 = r2_score(y_test_lags, y_pred)

    print("\n=== XGBoost Baseline ===")
    print(f"MAE (GWh): {mae:.4f}")
    print(f"RMSE (GWh): {rmse:.4f}")
    print(f"R²: {r2:.4f}")

    return mae, rmse, r2


