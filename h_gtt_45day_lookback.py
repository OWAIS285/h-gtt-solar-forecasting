def fetch_weather_data(api_url):
    response = requests.get(api_url)
    if response.status_code != 200:
        raise ValueError(f"Failed to fetch weather data. Response Code: {response.status_code}")

    df = pd.read_csv(io.StringIO(response.text))
    df = df[['temp', 'humidity', 'windspeed', 'cloudcover', 'solarradiation']]
    df = df.apply(pd.to_numeric, errors='coerce').dropna()

    if df.shape[0] < 5:
        raise ValueError(f"Insufficient data rows after cleaning. Found {df.shape[0]} rows.")

    return df[:5]


def predict_next_5_days(model, scaler, api_url, target_mean, target_std):
    raw_data = fetch_weather_data(api_url)
    scaled_data = torch.tensor(scaler.transform(raw_data), dtype=torch.float32)

    num_nodes = scaled_data.shape[0]
    if num_nodes < 2:
        raise ValueError("Need at least 2 rows for a time-chain graph.")

    edge_index = torch.tensor(
        [[i, i + 1] for i in range(num_nodes - 1)],
        dtype=torch.long
    ).t().contiguous()

    with torch.no_grad():
        predictions_norm = model(scaled_data, edge_index).squeeze().numpy()

    predictions = predictions_norm * target_std + target_mean
    return predictions

