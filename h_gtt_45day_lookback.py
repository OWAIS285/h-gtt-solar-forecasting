class HybridGTT(nn.Module):
    def __init__(self, in_features, gat_hidden_dim, transformer_hidden_dim, heads=4):
        super(HybridGTT, self).__init__()
        # GAT stack
        self.gat1 = GATConv(in_features, gat_hidden_dim, heads=heads)
        self.gat2 = GATConv(gat_hidden_dim * heads, gat_hidden_dim, heads=1)

        # TransformerConv stack
        self.trans1 = TransformerConv(gat_hidden_dim, transformer_hidden_dim)
        self.trans2 = TransformerConv(transformer_hidden_dim, transformer_hidden_dim)

        self.fc = nn.Linear(transformer_hidden_dim, 1)
        self.reset_parameters()

    def reset_parameters(self):
        self.gat1.reset_parameters()
        self.gat2.reset_parameters()
        self.trans1.reset_parameters()
        self.trans2.reset_parameters()
        nn.init.xavier_uniform_(self.fc.weight)
        nn.init.zeros_(self.fc.bias)

    def forward(self, x, edge_index):
        x = F.relu(self.gat1(x, edge_index))
        x = F.relu(self.gat2(x, edge_index))
        x = F.relu(self.trans1(x, edge_index))
        x = F.relu(self.trans2(x, edge_index))
        return self.fc(x)


class GATOnly(nn.Module):
    """Ablation: GAT + FC, no TransformerConv."""
    def __init__(self, in_features, gat_hidden_dim=64, heads=4):
        super().__init__()
        self.gat = GATConv(in_features, gat_hidden_dim, heads=heads)
        self.fc = nn.Linear(gat_hidden_dim * heads, 1)
        self.reset_parameters()

    def reset_parameters(self):
        self.gat.reset_parameters()
        nn.init.xavier_uniform_(self.fc.weight)
        nn.init.zeros_(self.fc.bias)

    def forward(self, x, edge_index):
        x = F.relu(self.gat(x, edge_index))
        return self.fc(x)


class TransformerOnly(nn.Module):
    """Ablation: TransformerConv + FC, no GAT."""
    def __init__(self, in_features, hidden_dim=64):
        super().__init__()
        self.transformer = TransformerConv(in_features, hidden_dim)
        self.fc = nn.Linear(hidden_dim, 1)
        self.reset_parameters()

    def reset_parameters(self):
        self.transformer.reset_parameters()
        nn.init.xavier_uniform_(self.fc.weight)
        nn.init.zeros_(self.fc.bias)

    def forward(self, x, edge_index):
        x = F.relu(self.transformer(x, edge_index))
        return self.fc(x)

def load_data(file_path):
    df = pd.read_csv(file_path)

    features = df[['temp', 'humidity', 'windspeed', 'cloudcover', 'solarradiation']].values.astype(np.float32)
    target = df['solarenergy(GWh)'].values.astype(np.float32)

    n = len(df)
    train_end = int(0.8 * n)   # 80% for train+val, 20% for test

    # Raw splits (no scaling yet)
    X_trainval_raw = features[:train_end]
    y_trainval_raw = target[:train_end]
    X_test_raw = features[train_end:]
    y_test_raw = target[train_end:]

    # Scale features on TRAIN+VAL only
    scaler = StandardScaler()
    X_trainval = scaler.fit_transform(X_trainval_raw)
    X_test = scaler.transform(X_test_raw)

    # Normalize target on TRAIN+VAL only
    target_mean = y_trainval_raw.mean()
    target_std = y_trainval_raw.std() if y_trainval_raw.std() > 0 else 1.0
    y_trainval = (y_trainval_raw - target_mean) / target_std
    y_test = (y_test_raw - target_mean) / target_std

    # Validation = last 20% of trainval
    n_trainval = len(X_trainval)
    val_size = int(0.2 * n_trainval)
    if val_size < 10:
        val_size = max(10, n_trainval // 5)

    X_train = X_trainval[:-val_size]
    y_train = y_trainval[:-val_size]
    X_val = X_trainval[-val_size:]
    y_val = y_trainval[-val_size:]

    # To tensors
    X_train = torch.tensor(X_train, dtype=torch.float32)
    X_val = torch.tensor(X_val, dtype=torch.float32)
    X_test = torch.tensor(X_test, dtype=torch.float32)
    y_train = torch.tensor(y_train, dtype=torch.float32)
    y_val = torch.tensor(y_val, dtype=torch.float32)
    y_test = torch.tensor(y_test, dtype=torch.float32)

    def make_edge_index(length):
        if length < 2:
            return torch.zeros((2, 0), dtype=torch.long)
        return torch.tensor(
            [[i, i + 1] for i in range(length - 1)],
            dtype=torch.long
        ).t().contiguous()

    edge_index_train = make_edge_index(len(X_train))
    edge_index_val = make_edge_index(len(X_val))
    edge_index_test = make_edge_index(len(X_test))

    return (X_train, y_train, edge_index_train,
            X_val, y_val, edge_index_val,
            X_test, y_test, edge_index_test,
            target_mean, target_std, scaler,
            y_trainval_raw, y_test_raw)

