# HELPER: LAG FEATURES FOR XGBOOST / LIGHTGBM
# =========================
def create_lag_features(X, y, lags=[1, 2, 3, 7]):
    """
    X: numpy array (N, num_features)
    y: numpy array (N,)
    Returns: X_lagged, y_lagged
    """
    X_lags = []
    y_lags = []
    max_lag = max(lags)
    for i in range(max_lag, len(X)):
        row = []
        for lag in lags:
            row.extend(X[i - lag])
        X_lags.append(row)
        y_lags.append(y[i])
    return np.array(X_lags), np.array(y_lags)


