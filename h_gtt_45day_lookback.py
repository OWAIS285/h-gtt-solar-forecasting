def train_arima_baseline(y_trainval_raw, y_test_raw):
    """
    Simple ARIMA(1,1,1) on raw target.
    Train on trainval, forecast len(test) steps.
    """
    model = ARIMA(y_trainval_raw, order=(1, 1, 1))
