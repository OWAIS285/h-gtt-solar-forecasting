def train_hybridgtt(data_path, num_epochs=300, patience=25, lr=0.001):
    (X_train, y_train, edge_index_train,
     X_val, y_val, edge_index_val,
     X_test, y_test, edge_index_test,
     target_mean, target_std, scaler,
     y_trainval_raw, y_test_raw) = load_data(data_path)

    model = HybridGTT(in_features=X_train.shape[1],
                      gat_hidden_dim=160,
                      transformer_hidden_dim=80)

    optimizer = torch.optim.Adam(model.parameters(), lr=lr)
    criterion = torch.nn.MSELoss()
    scheduler = torch.optim.lr_scheduler.ReduceLROnPlateau(
        optimizer, mode='min', factor=0.5, patience=5, verbose=False
    )

    best_val_mae = float('inf')
    best_state = None
    patience_counter = 0

    # history dict to record training progress
    history = {
        "train_loss": [],
        "train_mae": [],
        "val_loss": [],
        "val_mae": []
    }

    for epoch in range(1, num_epochs + 1):
        # ---- TRAIN ----
        model.train()
        optimizer.zero_grad()
        train_pred = model(X_train, edge_index_train).squeeze()
        train_loss = criterion(train_pred, y_train)
        train_loss.backward()
        optimizer.step()

        train_mae = mean_absolute_error(
            y_train.detach().cpu().numpy(),
            train_pred.detach().cpu().numpy()
        )

        # ---- VAL ----
        model.eval()
        with torch.no_grad():
            if len(X_val) > 1:
                val_pred = model(X_val, edge_index_val).squeeze()
                val_loss = criterion(val_pred, y_val).item()
                val_mae = mean_absolute_error(
                    y_val.detach().cpu().numpy(),
                    val_pred.detach().cpu().numpy()
                )
            else:
                val_loss = float('nan')
                val_mae = float('nan')

        scheduler_loss = torch.tensor(val_loss if not np.isnan(val_loss) else train_loss.item())
        scheduler.step(scheduler_loss)

        # store in history
        history["train_loss"].append(train_loss.item())
        history["train_mae"].append(train_mae)
        history["val_loss"].append(val_loss)
        history["val_mae"].append(val_mae)

        if epoch == 1 or epoch % 10 == 0:
            print(f"[HybridGTT] Epoch {epoch:03d} | "
                  f"Train Loss={train_loss.item():.4f} MAE={train_mae:.4f} | "
                  f"Val Loss={val_loss:.4f} MAE={val_mae:.4f}")

        # Early stopping on val MAE
        if not np.isnan(val_mae) and val_mae < best_val_mae:
            best_val_mae = val_mae
            best_state = model.state_dict()
            patience_counter = 0
        else:
            patience_counter += 1
            if patience_counter >= patience:
                print(f"[HybridGTT] Early stopping at epoch {epoch}")
                break

    if best_state is not None:
        model.load_state_dict(best_state)

    # ---- TEST EVAL ----
    model.eval()
    with torch.no_grad():
        y_pred_test_norm = model(X_test, edge_index_test).squeeze().cpu().numpy()
        y_true_test_norm = y_test.cpu().numpy()

    y_pred_test = y_pred_test_norm * target_std + target_mean
    y_true_test = y_true_test_norm * target_std + target_mean

    test_mae = mean_absolute_error(y_true_test, y_pred_test)
    test_rmse = np.sqrt(mean_squared_error(y_true_test, y_pred_test))
    test_r2 = r2_score(y_true_test, y_pred_test)

    print("\n=== FINAL TEST PERFORMANCE (HybridGTT) ===")
    print(f"MAE (GWh):  {test_mae:.4f}")
    print(f"RMSE (GWh): {test_rmse:.4f}")
    print(f"R²:         {test_r2:.4f}")

    return model, (X_test, edge_index_test, target_mean, target_std, scaler,
                   y_trainval_raw, y_test_raw, y_true_test, y_pred_test, history)

