def train_lstm_baseline(X_train, y_train, X_val, y_val,
                        X_test, y_test,
                        target_mean, target_std,
                        num_epochs=100, patience=20, lr=0.001):
    """
    X_*: torch tensors, shape (N, num_features) - SAME as from load_data
    y_*: torch tensors, normalized
    """
    # reshape to (N, seq_len=1, features)
    X_train_seq = X_train.unsqueeze(1)
    X_val_seq   = X_val.unsqueeze(1)
    X_test_seq  = X_test.unsqueeze(1)

    model = LSTMBaseline(in_features=X_train.shape[1], hidden_size=64, num_layers=2)
    optimizer = torch.optim.Adam(model.parameters(), lr=lr)
    criterion = torch.nn.MSELoss()

    best_val_mae = float('inf')
    best_state = None
    patience_counter = 0

    for epoch in range(1, num_epochs + 1):
        model.train()
        optimizer.zero_grad()
        train_pred = model(X_train_seq).squeeze()
        train_loss = criterion(train_pred, y_train)
        train_loss.backward()
        optimizer.step()

        train_mae = mean_absolute_error(
            y_train.detach().cpu().numpy(),
            train_pred.detach().cpu().numpy()
        )

        model.eval()
        with torch.no_grad():
            val_pred = model(X_val_seq).squeeze()
            val_loss = criterion(val_pred, y_val).item()
            val_mae = mean_absolute_error(
                y_val.detach().cpu().numpy(),
                val_pred.detach().cpu().numpy()
            )

        if epoch == 1 or epoch % 10 == 0:
            print(f"[LSTM] Epoch {epoch:03d} | "
                  f"Train Loss={train_loss.item():.4f} MAE={train_mae:.4f} | "
                  f"Val Loss={val_loss:.4f} MAE={val_mae:.4f}")

        if val_mae < best_val_mae:
            best_val_mae = val_mae
            best_state = model.state_dict()
            patience_counter = 0
        else:
            patience_counter += 1
            if patience_counter >= patience:
                print(f"[LSTM] Early stopping at epoch {epoch}")
                break

    if best_state is not None:
        model.load_state_dict(best_state)

    # Test evaluation (de-normalize)
    model.eval()
    with torch.no_grad():
        y_pred_test_norm = model(X_test_seq).squeeze().cpu().numpy()
        y_true_test_norm = y_test.cpu().numpy()

    y_pred_test = y_pred_test_norm * target_std + target_mean
    y_true_test = y_true_test_norm * target_std + target_mean

    mae = mean_absolute_error(y_true_test, y_pred_test)
    rmse = np.sqrt(mean_squared_error(y_true_test, y_pred_test))
    r2 = r2_score(y_true_test, y_pred_test)

    print("\n=== FINAL TEST PERFORMANCE (LSTM) ===")
    print(f"MAE (GWh): {mae:.4f}")
    print(f"RMSE (GWh): {rmse:.4f}")
    print(f"R²: {r2:.4f}")

    return mae, rmse, r2


